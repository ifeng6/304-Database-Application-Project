CREATE TABLE Users(
	username VARCHAR(20),
	email VARCHAR(320),
	date_joined DATE,
	age NUMBER,
	gender VARCHAR(20),
	PRIMARY KEY (username),
    UNIQUE (email)
);

CREATE TABLE Media(
	mID NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 100),
	date_released DATE,
	genre VARCHAR(200),
	title VARCHAR(40),
	PRIMARY KEY (mID)
);

CREATE TABLE Reviews(
	username VARCHAR(20),
    mID NUMBER,
    date_created DATE,
	rating NUMBER,
	title VARCHAR(40),
	comments VARCHAR(4000),
	PRIMARY KEY (username, mID),
    FOREIGN KEY (username) REFERENCES Users(username)
	    ON DELETE CASCADE,
    FOREIGN KEY (mID) REFERENCES Media(mID)
	    ON DELETE CASCADE
);

CREATE TABLE DiscussionBoard(
    title VARCHAR(50),
	topic VARCHAR(40),
	date_of_creation DATE,
	days_inactive NUMBER,
    PRIMARY KEY (title)
);

CREATE TABLE BoardStatus(
	days_inactive NUMBER,
    BoardStatus VARCHAR(40),
    PRIMARY KEY (days_inactive)
);

CREATE TABLE Popularity(
    number_of_upvotes NUMBER, 
    number_of_replies NUMBER, 
    popularity NUMBER,
    PRIMARY KEY (number_of_upvotes, number_of_replies)
);

CREATE TABLE Post(
    pID NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 100), 
    number_of_upvotes NUMBER, 
    number_of_replies NUMBER, 
    title VARCHAR(50), 
    content VARCHAR(200), 
    PRIMARY KEY (pID)
);

CREATE TABLE Community(
	name VARCHAR(40),
	admin VARCHAR(20),
	description VARCHAR(200),
	number_of_users NUMBER,
	date_created DATE,
	PRIMARY KEY (name),
	FOREIGN KEY (admin) REFERENCES Users(username)
);

CREATE TABLE CrewMember(
	cID NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 100),
	firstName VARCHAR(100),
	lastName VARCHAR(100),
	PRIMARY KEY (cID)
);

CREATE TABLE Studio(
	name VARCHAR(100),
	headquarters VARCHAR(200),
	number_of_employees NUMBER,
	date_founded DATE,
	PRIMARY KEY (name)
);

CREATE TABLE Show(
    mID NUMBER,
    number_of_episodes NUMBER,
    platforms VARCHAR(80),
    PRIMARY KEY (mID),
    FOREIGN KEY (mID) REFERENCES Media(mID) ON DELETE CASCADE
);

CREATE TABLE Movie(
    mID NUMBER,
    box_office_gross NUMBER,
    runtime NUMBER,
    PRIMARY KEY (mID),
    FOREIGN KEY (mID) REFERENCES Media(mID) ON DELETE CASCADE
);

CREATE TABLE Reliability(
    average_rating REAL,
    number_of_reviews NUMBER,
    reliability REAL,
    PRIMARY KEY (average_rating, number_of_reviews)
);

CREATE TABLE HasEpisode(
	title VARCHAR(40),
	mID NUMBER,
	description VARCHAR(200),
	length NUMBER,
	season NUMBER,
	episode_number NUMBER,
    PRIMARY KEY (title, mID),
    FOREIGN KEY (mID) REFERENCES Media(mID)
	    ON DELETE CASCADE
);

CREATE TABLE MemberOf(
	username VARCHAR(20),
	name VARCHAR(40),
	PRIMARY KEY (username, name),
	FOREIGN KEY (username) REFERENCES Users(username)
        ON DELETE CASCADE,
	FOREIGN KEY (name) REFERENCES Community(name)
        ON DELETE CASCADE
);

CREATE TABLE Contain(
	title VARCHAR(40) NOT NULL,
	pID NUMBER,
	PRIMARY KEY (pID),
	FOREIGN KEY (pID) REFERENCES Post(pID)
        ON DELETE CASCADE,
	FOREIGN KEY (title) REFERENCES DiscussionBoard(title)
		ON DELETE CASCADE
);

CREATE TABLE MakesPost( 
    date_posted DATE,
    username VARCHAR(20) NOT NULL,
    pID NUMBER,
    PRIMARY KEY (pID),
    FOREIGN KEY (pID) REFERENCES Post(pID)
    ON DELETE CASCADE,
    FOREIGN KEY (username) REFERENCES Users(username) 
    	ON DELETE CASCADE
);

CREATE TABLE HasFavourite(
    username VARCHAR(20),
    cID NUMBER,
    PRIMARY KEY (username, cID),
    FOREIGN KEY (username) REFERENCES Users(username)
    ON DELETE CASCADE,
    FOREIGN KEY (cID) REFERENCES CrewMember(cID)
	ON DELETE CASCADE
);

CREATE TABLE WorkedOn(
    cID NUMBER,
    mID NUMBER,
    role VARCHAR(50),
    compensation NUMBER,
    PRIMARY KEY (cID, mID),
    FOREIGN KEY (cID) REFERENCES CrewMember(cID)
    ON DELETE CASCADE,
    FOREIGN KEY (mID) REFERENCES Media(mID)
	ON DELETE CASCADE
);

CREATE TABLE Produced(
    name VARCHAR(40), 
    mID NUMBER, 
    budget NUMBER, 
    time_spent NUMBER,
    PRIMARY KEY (name, mID),
    FOREIGN KEY (name) references Studio
    ON DELETE CASCADE,
    FOREIGN KEY (mID) references Media
	ON DELETE CASCADE
);

